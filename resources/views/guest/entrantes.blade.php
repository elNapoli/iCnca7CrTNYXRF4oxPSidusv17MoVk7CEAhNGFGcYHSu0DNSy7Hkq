<p align='justify'>De acuerdo al Consejo Nacional de Educación – CNED, el sistema de educación superior considera tres tipos de instituciones de educación superior: universidades, institutos profesionales y centros de formación técnica, y reconoce oficialmente tres tipos de certificaciones académicas: títulos técnicos de nivel superior, títulos profesionales y grados académicos.</p>

<p align='justify'>Además, la ley reconoce oficialmente como instituciones de educación superior a los establecimientos de las Fuerzas Armadas, de la Dirección General de Aeronáutica Civil, de Carabineros y de la Policía de Investigaciones. Ellos están facultados para entregar títulos y grados académicos propios del ámbito de su competencia, según corresponda.</p>

<p align='justify'>En nuestro país, las universidades se encuentran facultadas para otorgar toda clase de títulos y grados académicos, los institutos profesionales sólo pueden otorgar títulos profesionales (con excepción de aquellos reservados únicamente para las universidades) y títulos técnicos de nivel superior, mientras que los centros de formación técnica sólo se encuentran habilitados para entregar títulos técnicos de nivel superior.</p>

<p align='justify'>Los grados académicos definidos por la ley son los de licenciado, magíster y doctor. El grado de licenciado apunta a entregar al egresado una formación en los aspectos esenciales de un área del conocimiento o disciplina que se trate. El grado de magíster requiere estar previamente en posesión de un grado de licenciado o un título profesional equivalente, y se otorga al alumno que ha aprobado un programa de estudios de profundización en una o más disciplinas. El grado de doctor requiere la obtención previa del grado de licenciado, y acredita que quien lo posee es capaz de efectuar investigaciones originales. El programa de doctorado requiere la elaboración y defensa de una tesis que contribuya a la disciplina estudiada.</p>

<p align='justify'>Los grados académicos son otorgados exclusivamente por las universidades.</p>

<p align='justify'>Los títulos profesionales apuntan a entregar una formación general y científica para un adecuado desempeño profesional. Existe un conjunto de títulos profesionales definidos en la ley que requieren haber obtenido previamente el grado académico de licenciado, por lo que sólo pueden ser otorgados por una universidad.</p>

<p align='justify'>Los títulos técnicos de nivel superior son otorgados tras cursar un programa de estudios de 1600 clases que le confiere a sus egresados la capacidad y conocimientos para desempeñarse como apoyo al nivel profesional.</p>
