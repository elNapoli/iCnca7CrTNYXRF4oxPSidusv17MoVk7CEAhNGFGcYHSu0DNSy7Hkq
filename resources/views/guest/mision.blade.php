	<p>La Oficina de Movilidad Estudiantil de la Universidad Austral de Chile (UACh), está a cargo de coordinar y ejecutar los programas de intercambio para estudiantes UACh, extranjeros y nacionales, tanto a nivel de pregrado y postgrado. </p>

	<h2 id='sect'>Misión</h2>

	<br>
	<p>Informar, guiar y difundir entre los estudiantes las posibilidades de movilidad estudiantil en el contexto de convenios bilaterales firmados con universidades extranjeras y nacionales. Asi mismo, facilitar a los estudiantes el apoyo necesario para concretar la actividad académica en la universidad de destino.</p>

	<h2 id='sect'>Objetivos</h2>

	<br>
	<p>Ofrecer a los estudiantes universitarios la oportunidad de realizar estudios parciales de pregrado en otras universidades del país o en el extranjeros, con las cuales existen convenios, creando un espacio para el reconocimiento pleno de los programas académicos cursados.</p>
	<p>Dar a los estudiantes la oportunidad de ampliar sus horizontes en el plano académico y personal, fortaleciendo su capacidad de adaptación a diferentes contextos y de ampliar sus visiones de otras regiones del mundo.</p>
	<p>Participar de los avances científicos de otras universidades del mundo, perfeccionando así su formación profesional.</p>

